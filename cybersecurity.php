            <h1 class="main_heading m-3">What is cyberSecurity?</h1>

            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <li class="para">  Cyber security is a broad-spectrum phrase and relates to preventing any form of unauthorized and malafide access to a personal computer, a laptop, a smartphone or a major network 
                    like the national banking system or the railway network or a national information technology asset that also has military implications. </li>

                <li class="para">Cyber refers to the technology that includes systems, networks, programs, and data. And security is concerned with the protection of systems, networks, applications, and information. In some cases, it is also called electronic information security or information technology security.</li>

                <li class="para">Cyber-attack is now an international concern. It has given many concerns that could endanger the global economy. As the volume of cyber-attacks grows, companies and organizations, especially those that deal with information related to national security,
                    health, or financial records, need to take steps to protect their sensitive business and personal information.</li>


                <center><img src="/techwix/assets/images/cybersecurity3.jpg" alt=" " style="width:600px;height:300px;border-radius: 50%"></center>
