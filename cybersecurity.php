            <h1 class="main_heading m-3">CyberSecurity</h1>
            <p class="para" style="margin-left: 1%;text-align: justify;">Cyber Security refers to <span style="color:black;font-weight:bold">protecting cyber space including critical information infrastructure </span> from attack, damage, misuse and economic espionage.</p>
            <div class="container" style="margin-left:13%">
            <img src="/techwix/assets/images/cybersecurity1.jpeg" class="image1 col-sm-6" style="width:350px;border-radius: 50%;">
