            <h1 class="main_heading m-3">What is the need of cybersecurity?</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <li class="para"> • Financial services, payments, health services, etc are all connected to digital mediums; and due to Covid-19, this is expected to increase. </li>

                <li class="para">•  There has been a rapid increase in the use of the online environment where millions of users have access to internet resources and are providing contents on a daily basis.</li>

                <li class="para">•  To ensure critical infrastructure system do not collapse under any situation.</li>
                   <ol>
                    <li>•   To ensure Business continuity.
                    </li>

                    <li>•   To balance Individual’s rights, liberty and privacy.</li>
                    <li> Cyber Security's main objective is to ensure data protection. The security community provides a triangle of three related principles to protect the data from cyber-attacks. This principle is called the CIA triad. 
                   These goals form the confidentiality, integrity, availability (CIA) triad, the basis of all security programs.This model is also referred to as the AIC (Availability, Integrity, and Confidentiality) triad to avoid the confusion with the Central Intelligence Agency.</li>
                   </ol>
