            </ul>

            <h1 class="main_heading m-3">Types of cyber Attracks</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <p> A threat in cybersecurity is a malicious activity by an individual or organization to corrupt or steal data, gain access to a network, or disrupts digital life in general. The cyber community defines the following threats available today:</p>
                 
                <li><span style="color:black;font-weight:bold;font-size:16px">1.Virus:</span> It is a malicious piece of code that spreads from one device to another. It can clean files and spreads throughout a computer system, infecting files, stoles information, or damage device.</li>
                <li><span style="color:black;font-weight:bold;font-size:16px">2.Phishing:</span> Phishing is a type of cybercrime in which a sender seems to come from a genuine organization like PayPal, eBay, financial institutions, or friends and co-workers. They contact a target or targets via email, phone, or text message with a link to persuade them to click on that links. 
                    Clicking on the link will also install malware on the target devices that allow hackers to control devices remotely.
                </li>
                <li><span style="color:black;font-weight:bold;font-size:16px">3.Domain Name System:</span>
                    A DNS attack is a type of cyberattack in which cyber criminals take advantage of flaws in the Domain Name System to redirect site users to malicious websites (DNS hijacking) and steal data from affected computers. It is a severe cybersecurity risk because the DNS system is an essential element of the internet infrastructure.</li>
                <center><img src="/techwix/assets/images/cybersecurity5.jpeg" alt="ux" style="width:600px;height:400px;border-radius:50px;margin-top:2%"></center>
               </div>
            </ul>


            <h1 class="main_heading m-3" style="margin-right: 2%;">Protect yourself against Cyberattacks:</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;padding:12px">
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Update your software and operating system:</span> Software updates often include security patches that can help protect your data. </li>

                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Use anti-virus software:</span> Some security solutions will detect and removes threats. Keep your software updated for the best level of protection.</li>
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Use strong passwords:</span> Passwords should be at least eight characters long and include a mix of uppercase and lowercase letters, numbers, and symbols. Avoid using easily guessed words like your name or birthdate. </li>
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Use Two-factor Authentication:</span> Two-factor authentication adds an extra layer of security by requiring you to enter a code from your phone or email in addition to your password. </li>
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Be Cautious of Phishing Scams:</span> Phishing scams are emails or websites that pose as a legitimate company to trick you into entering your personal information. </li>
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Avoid using unsecure WiFi networks in public places:</span> Unsecure networks leave you vulnerable to man-in-the-middle attacks.</li>

