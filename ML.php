            <h1 class="main_heading m-3">What is Machine Learning?</h1>

            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <li class="para">  A Machine Learning system learns from historical data, builds the prediction models, and whenever it receives new data, predicts the output for it. 
                    The accuracy of predicted output depends upon the amount of data, as the huge amount of data helps to build a better model which predicts the output more accurately.
                "Machine learning enables a machine to automatically learn from data, improve performance from experiences, and predict things without being explicitly programmed." </li>

                <li class="para">Machine learning is making our day to day life easy from self-driving cars to Amazon virtual assistant "Alexa". However, the idea behind machine learning is so old and has a long history.
                    Machine learning is an application of artificial intelligence that uses statistical techniques to enable computers to learn and make decisions without being explicitly programmed. 
                </li>

                <li class="para">Machine Learning is a subset of Artifitical Intelligence.If good quality data is fed to the machines, and different algorithms are used to build ML models to train the machines on this data. The choice of algorithm depends on the type of data at hand and the type of activity that needs to be automated. </li>


