            <h1 class="main_heading m-3">Types of Machine Learning</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;margin-top:2%">
                <p> At a broad level, machine learning can be classified into three types:</p>
                 
                <li><span style="color:black;font-weight:bold;font: size 16px">Supervised learning:</span> Supervised learning is a type of machine learning in which the algorithm is trained on the labeled dataset. It learns to map input features to targets based on labeled training data. 
                    In supervised learning, the algorithm is provided with input features and corresponding output labels, and it learns to generalize from this data to make predictions on new, unseen data.There are further classified into two types:</li>
                        <li style="list-style-type: circle;margin-left:5%;color:grey">Regression</li>
                        <li style="list-style-type: circle;margin-left:5%;color:grey">Classification</li>
                    
                <li><span style="color:black;font-weight:bold;font-size:16px"> Unsupervised Machine Learning:</span> Unsupervised learning is a type of machine learning where the algorithm learns to recognize patterns in data without being explicitly trained using labeled examples. The goal of unsupervised 
                    learning is to discover the underlying structure or distribution in the data.There are two main types of unsupervised learning:</li>
                    <li style="list-style-type: circle;margin-left:5%;color:grey">Clustering</li>
                    <li style="list-style-type: circle;margin-left:5%;color:grey">Dimensionality reduction</li>

                <li><span style="color:black;font-weight:bold;font-size:16px">Reinforcement Machine Learning:</span>
                    Reinforcement learning is a type of machine learning where an agent learns to interact with an environment by performing actions and receiving rewards or penalties based on its actions. The goal of reinforcement learning is to learn a policy, which is a mapping from states to actions, that maximizes the expected cumulative reward over time.</li>
                    <li style="list-style-type: circle;margin-left:5%;color:grey">Model-based reinforcement learning</li>
                    <li style="list-style-type: circle;margin-left:5%;color:grey">Model-free reinforcement learning</li>
    
                <center><img src="/techwix/assets/images/ML3.jpg" alt="ux" style="width:600px;height:400px;border-radius:50px;margin-top:2%"></center>
               </div>
            </ul>


