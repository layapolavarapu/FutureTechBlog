            <h1 class="main_heading m-3">Machine Learning</h1>
            <p class="para" style="margin-left: 1%;text-align: justify;"><span style="color:black;font-weight:bold">Machine Learning</span>  is a growing technology which enables computers to learn automatically from past data. Machine learning uses various algorithms for <span style="color:black;font-weight:bold">building 
                mathematical models and making predictions using historical data or information.</span>
           </p>
            <div class="container">
