            <h2 class="main_heading m-3" style="margin-right: 2%;">Limitations of Machine Learning:</h2>
            
                <div style="margin-left: 1%;text-align: justify;">
                    <ol>
                        <li>1.The primary challenge of machine learning is the lack of data or the diversity in the dataset.</li>
                        <li>2. A machine cannot learn if there is no data available. Besides, a dataset with a lack of diversity gives the machine a hard time. </li>
                        <li>3.A machine needs to have heterogeneity to learn meaningful insight. </li>
                        <li>4.It is rare that an algorithm can extract information when there are no or few variations.</li>
                        <li>5. It is recommended to have at least 20 observations per group to help the machine learn. This constraint leads to poor evaluation and prediction.
