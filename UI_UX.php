            <h1 class="main_heading m-3">UI/UX DESIGN</h1>
            <p class="para" style="margin-left: 1%;text-align: justify;">UX stands for “user experience,” and UI stands for “user interface.” You’ll often see them lumped together as they are two closely related professions that often work hand-in-hand.</p>
            <div class="container" style="margin-left:13%">
            <img src="/techwix/assets/images/UI_UX.jpg" class="image1 col-sm-6" style="width:400px;border-radius: 50%;">
            <img src="/techwix/assets/images/ui_ux.jpeg" class="image1 col-sm-6" style="width:400px;border-radius: 50%;">
            </div>
            <h1 class="main_heading m-3">What is UX design?</h1>

            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <li class="para"> User experience(UX) design is the process of planning the experience a person has when they interact with a product. </li>

                <li class="para">UX design focuses on the interaction that a human user has with everyday products and services. The goal of UX design is to make using these products and services, both digital or physical, easy, logical, and fun.</li>

                <li class="para">The user is the person who is going to live, eat, and breathe your products. It’s your job as a UX designer to give them an enjoyable, useful experience.</li>

                <li class="para"> But first, you have to know who they are. Designing a user persona (which is done by a UX researcher, whose role is more back-end and data-based) lets us come up with an ideal user and examine their desires, wants, and frustrations with current solutions.</li><br>

                <center><img src="/techwix/assets/images/ux.jpg" alt="ux" style="width:600px;height:300px"></center>
               </div>
            </ul>
            

            <h1 class="main_heading m-3">What is UI design?</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <li class="para"> UI (User Interface) design is the user-centered approach to designing the aesthetics of a digital product. In essence, they create the look and feel of a website or application’s user interface. An interface is the graphical layout of the application. These interfaces should not only be functional, but they should be easy to use and visually appealing. </li>

                <li class="para">UI designers are focused on visual touchpoints that let users interact with a product. This can include typography, color palettes, buttons, animation, and other imagery. Think about all the things you might do on an app – slide to delete, pull down to refresh, enter text, etc. All of these visual elements or animations that allow you to interact with the app must be designed. There’s a lot of similarities between UI and graphic design, but they are not the same thing.</li>

                <li class="para">User interface can also refer to other interfaces:</li>
                   <ol>
                    <li>Voice-controlled interfaces (i.e. Siri, Alexa, and Google Assistant.)
                    </li>

                    <li>Voice user interfaces (VUI’s) are currently improving the user experience, making it easier and faster for users to get the information they need or complete certain tasks. However, for the purposes of this article, we’re sticking to digital interfaces (screens).</li>

                   </ol>
                <center><img src="/techwix/assets/images/ui.webp" alt="ui" style="width:600px;height:500px"></center>
               </div>
            </ul>

            <h1 class="main_heading m-3">What do UX designers do?</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <p> If you have a background (or interest) in design, brand strategy, or technology, then becoming a UX (User Experience) designer may be the job for you. But first, what does a UX designer actually do? </p>


                <li class="para"><span class="sub_heading"><b>UX designers act as advocates for the user:</b></span>Sometimes, you (the user) feel like you’re the problem when you can’t figure out how to use a product or website. Most of the time, though, it’s not your fault. It’s the UX designer’s job to design with you in mind. They are there to make products and technology not only useful, but enjoyable and easy to use.</li><br>

                <center><img src="/techwix/assets/images/ux1.jpg" alt="ux" style="width:600px;height:400px"></center>

                <li class="para"><b>In sum, UX designers have a wide range of job responsibilities:</b></li>
                   <ol>
                    <li>Design user personas and conducting user research.</li>

                    <li>Design user flows and wireframes.</li>

                    <li>Create product prototypes.</li>

                    <li>Test products on real users.</li>

                    <li>Improve products over time through continuous testing.</li>

                   </ol><br>
               </div>
            </ul>


            <h1 class="main_heading m-3">What do UI designers do?</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                    <p>UI designers often have a background in graphic design, interior design, or visual arts. They are responsible for making user interfaces that are easy to use and satisfying for the user. These are graphical in nature, and include websites, apps, and video games, to name a few.</p>

                    <b>It’s the UI designer’s job to bring the UX designer’s ideas to life. </b><br>
                <li class="para"> Once the UX team has finished their process and hands over a wireframe, UI Designers are in charge of designing how the product is laid out visually. On websites, they are in charge of the elements on each screen or page in which a user interacts.</li>

                <li class="para">Of course, they must design from the user’s point of view. So, although they might be great artists, the design is about the user, not them. What do users desire? They want to navigate a site with ease, intuitively clicking through the pages and finding the information they need without a thought. Therefore, it’s your job as a UI designer to provide just that: an easily navigable product that’s so intuitive it’s almost invisible!</li><br>

                <li class="para">This includes incorporating basic design principles (like balance and contrast) and studying interaction design. It also means carefully choosing the typography, menu styling, buttons, icons, etc. to both represent the brand and satiate the user.</li><br>


