               <h1 class="main_heading m-3">Data Analyst Vs Data Scientist Vs Data Engineers</h1>
               <center><img src="assets/images/Data-science_vs_analyst-engineer.jpeg" alt="applications of data science"style="width:600px;height:300px" ></center>


               <!-- DATA ANALYST -->
               <h3>Data Analyst:</h3>
                <p>The role of the <strong>Data Analyst</strong> (also called Business Analyst) can be included among the “users” of the data, or those who answer the business questions relating to the present / past moment (i.e. “How are sales? What has been the trend of the last years … “) through the <strong>analysis, polishing and visualization</strong> of the collected data.</p>

                <p><b>The main activities of the Data Analyst’s activity are:</b></p>
                <ul style="list-style-type:disc;margin-left: 3%;">
                    <li>Screening and cleaning/polishing of the raw data collected</li>
                    <li>Data Preparation (data wrangling / munging)</li>
                    <li>Understanding of business metrics and problems</li>
                    <li>Visualization of data through reports and graphs</li>
                    <li>Identification of trends and useful suggestions to aid in strategic business decisions.</li>
                </ul>

                <h5><i style="font-weight: bolder;">What are the skills required for this figure?</i></h5>

                <p>Surely, a Data Analyst must have sufficient statistical / mathematical / economic training in order to understand the dynamics of said business and interpreting the inherent data.</p>


                <h5><i style="font-weight: bolder;">What tools are used?</i></h5>
                <p>It is essential to know Excel and SQL (query language used to manage data in a relational database).</p>

                <p>Business Intelligence tools can also be used, such as Tableau, Power BI, QlikView and many others, for creating dashboards, or Python and R to clean/polish and analyze data in greater depth.</p>

                <p> Data analyst is an individual, who performs mining of huge amount of data, models the data, looks for patterns, relationship, trends, and so on. At the end of the day, he comes up with visualization and reporting for analyzing the data for decision making and problem-solving process.</p>
           
               <!-- DATA SCIENTIST -->  
               <h3>Data Scientist:</h3>
                <p>The role of the Data Scientist also belongs to the ranks of data users. Unlike the Data Analyst, however, the Data Scientist deals with finding solutions regarding the future trend of the business.</p>

                <p>Through Machine Learning, Deep Learning techniques and Inferential Modeling, the Data Scientist can find correlations between data and create predictive models on the basis of which he/she can develop recommendation systems useful for said business.</p>

                <h5><i style="font-weight: bolder;">What are the skills required for this figure?</i></h5>

                <p>To fill this role it is necessary to have in-depth knowledge of statistics, mathematics and programming. Knowledge of Natural Language Processing (NLP), prescriptive and predicting modeling and multivariate statistics are also very important.</p>

                <h5><i style="font-weight: bolder;">What tools are used?</i></h5>

                <p>The Data Scientist also uses SQL, as well as having an advanced knowledge of Python and R. Knowledge of software development and Cloud Services are certainly also useful.</p>
    
            <!-- DATA ENGINEER -->  
               <h3>Data Engineer:</h3>
                <p>The flow of data used by Data Scientists and Data Analysts for their activities is structured by the role of the Data Engineer.</p>

                <p>Data Engineers, in fact, build the pipelines from which it is possible to import the information that comes from the users’ devices, thus designing the infrastructure of the data that is collected in the database. Basically, everything that happens to the data before it reaches the database is taken care of by the Data Engineer.</p>

                <h5><i style="font-weight: bolder;">What are the skills required for this figure?</i></h5>

                <p>To become a Data Engineer, a thorough knowledge of programming languages and data architecture tools are required. It is also important to have cloud computing and software development skills.</p>

                <h5><i style="font-weight: bolder;">What tools are used?</i></h5>

                <p>This figure uses SQL related tools, such as MySQL and PostgreSQL; he/she also should know technologies related to non-relational databases (NoSQL), such as MongoDB and Cassandra; and they use data tools such as Hive, Spark, Kafka, Pig and Map Reduce; As for cloud technologies, tools such as AWS, Google Cloud and Microsoft Azure are used.</p><br><br>
    

