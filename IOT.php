            <h1 class="main_heading m-3">What is the Internet of Things (IoT)?</h1>
            <center><img src="assets/images/what is iot.jpeg" class="image1 col-sm-6" style="width:400px; align-items:center;" alt="what is iot?"></center>

            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <li class="para"> The term IoT, or Internet of Things, refers to the collective network of connected devices and the technology that facilitates communication between devices and the cloud, as well as between the devices themselves. Thanks to the advent of inexpensive computer chips and high bandwidth telecommunication, we now have billions of devices  connected to the internet. This means everyday devices like toothbrushes, vacuums, cars, and machines can use sensors to collect data and respond intelligently to users.  </li>

                

