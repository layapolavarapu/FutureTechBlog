            <h1 class="main_heading m-3">Internet of Things</h1>
            <center><img src="assets/images/IOT.jpg" class="image1 col-sm-6" style="width:400px; align-items:center;" alt="iot"></center>
            <p class="para" style="margin-left: 1%;text-align: justify;">The Internet of Things (IoT) refers to the network of physical objects or "things" embedded with sensors, software, and connectivity capabilities that enable them to collect and exchange data over the internet. These objects can be everyday devices such as household appliances, vehicles, industrial machinery, or even wearable devices.</p>
            <p class="para" style="margin-left: 1%;text-align: justify;">The primary idea behind IoT is to create a seamless connection between the physical and digital worlds, allowing for greater automation, control, and efficiency. By connecting these objects and enabling them to communicate and share data, IoT enables a wide range of applications and benefits across various industries and sectors.</p>
