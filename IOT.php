            <h1 class="main_heading m-3">How Does the Internet of Things Work?</h1>
            <center><img src="assets/images/iot_working.jpeg" class="image1 col-sm-6" style="width:400px; align-items:center;padding:20px;" alt="what is iot?"></center>
                <p>We have heard many things in fiction from the past. It includes our cars automatically choosing the least congested route to work or home, automatic alarms, smart homes that do what we want at just a single voice command and many more. These things don’t feel like fiction anymore, and we can use these in our day to day activities now. All of these things come under the umbrella of Internet of Things applications.<br>
                     IoT Platform is made of four fundamental components. These are:</p>
                    <ol ><li style="font-size:large; list-style-type:disc"><strong>Sensors/Devices:</strong></li>
                            <p>Sensors like RFID are present everywhere, and these sensors collect very minute data from all their surroundings. This data can range from something as simple as the temperature to something much more complex such as video footage.<br> A device might also have a number of sensors which can act together to sense things or even go a step further from sensing things.<br> For example, your phone has a number of sensors like GPS, accelerometer, gyroscope, and many other which it, in turn, manages together to do more than just sense things.<br> The function of sensors, though, will always remain the same as sensing things no matter whether the device has a single sensor or multiple ones.</p>
                            <li style="font-size:large; list-style-type:disc"><strong>Connectivity</strong></li>
                            <p>This collected data from all the sensors is sent to the clouds where the data is stored and, but this transfer requires a medium. These sensors can be connected to the cloud infrastructure via cellular networks, WiFi, Bluetooth, satellite networks, and many more.<br> Each of these connectivity mediums has some trade-offs, and thus, it is essential to pick the right medium.</p>
                            <li style="font-size:large; list-style-type:disc"><strong>Data Processing</strong></li>
                            <p>Once the data reaches the cloud, the cloud starts analyzing it. This data processing can be quite simple or elaborate depending on the type of data collected by the sensor. It can be something as simple as determining if the temperature is within limits or something complicated like identifying an object in a video footage.</p>
                            <li style="font-size:large; list-style-type:disc"><strong>User Interface</strong></li>
                            <p>The final component is the user interface. All this collected information and its analysis must reach the end user for the user to act upon it. This is where the user interface comes into the picture. This user interface most often is either in a web browser or can be in as an app as well. This user interface shows you all the necessary data and also offers you options to make changes to the conditions. For example, if the temperature of the AC is too high, the app can let you adjust it to something more suitable.</p>
                        </ol>
