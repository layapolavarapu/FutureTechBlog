            <h1 class="main_heading m-3">Cloud Computing</h1>
            <p class="para" style="margin-left: 1%;text-align: justify;"><span style="color:black;font-weight:bold">Cloud Computing </span>  is a virtualization-based technology that allows us to create, configure, and customize applications via an internet connection. The cloud technology includes a development platform, 
            hard disk, software application, and database.</p>
            <div class="container">
        <center> <img src="/techwix/assets/images/cloudcomputing3.jpeg" class="image1 col-sm-12" style="width:350px;border-radius: 50%;"></center>
            
            </div>
            <h1 class="main_heading m-3">What is Cloud Computing?</h1>

            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <li class="para">  computing is the delivery of computing services—including servers, storage, databases, networking, software, analytics, and intelligence—over the Internet (“the cloud”) to offer faster innovation, flexible resources, 
                    and economies of scale. You typically pay only for cloud services you use, helping you lower your operating costs, run your 
                    infrastructure more efficiently, and scale as your business needs change. </li>

                <li class="para">The term cloud refers to a network or the internet. It is a technology that uses remote servers on the internet to store, manage, and access data online rather than local drives. The data can be anything such as files, images, documents, audio, video, and more.</li>

                <li class="para">Cloud computing provides the speed, scalability, and flexibility that enables businesses to develop, innovate, and support business IT solutions.</li>
               </div>
            </ul>
            

            <h1 class="main_heading m-3">Types of Cloud Computing</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;">
                <p class="para">Not all clouds are the same and no single type of cloud computing is right for everyone. Several different models, types, and services have evolved to help 
                    offer the right solution for your needs.There are three types of clouds: public, private, and hybrid. Each type requires a different level of management from the customer and provides a different level of security.</p>
                <h1 style="font-size: 19px;margin-top:2%">Public Cloud</h1>
                <p class="para">In a public cloud, the entire computing infrastructure is located on the premises of the cloud provider, and the provider delivers services to the customer over the internet. Customers do not have to maintain their own IT and can quickly add more users or computing power as needed. 
                    With a public cloud, all hardware, software, and other supporting infrastructure is owned and managed by the cloud provider</p>
                <h1 style="font-size: 19px;margin-top:2%">Private Cloud</h1>
                <p class="para">A private cloud refers to cloud computing resources used exclusively by a single business or organization. A private cloud is one in which the services and infrastructure are maintained on a private network. It could be hosted at the organization’s location or at the cloud provider’s data center. 
                    A private cloud provides the highest level of security and control.</p>
                <h1 style="font-size: 19px;margin-top:2%">Hybrid cloud</h1>
                <p class="para">Hybrid Cloud combine public and private clouds, bound together by technology that allows data and applications to be shared between them. By allowing data and applications to move between private and public clouds, a hybrid cloud gives your 
                    business greater flexibility and more deployment options and helps optimize your existing infrastructure, security, and compliance. </p>
                <h1 style="font-size: 19px;margin-top:2%">community Cloud</h1>
                <p class="para">Community cloud is a cloud infrastructure that allows systems and services to be accessible by a group of several organizations to share the information. It is owned, managed, and operated by one or
                     more organizations in the community, a third party, or a combination of them.The community cloud is flexible and scalable because it is compatible with every user. It allows the users to modify the documents as per their needs and requirement.</p>
                <center><img src="/techwix/assets/images/cloudcomputing4.png" alt=" " style="width:600px;height:500px"></center>
               </div>
            </ul>

            <h1 class="main_heading m-3">Types of cloud services</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;margin-top:2%">
                <p> Most cloud computing services fall into four broad categories: infrastructure as a service (IaaS), platform as a service (PaaS), serverless, and software as a service (SaaS). </p>
                 
                <li><span style="color:black;font-weight:bold;font: size 16px">1.IaaS:</span> The most basic category of cloud computing services. With infrastructure as a service (IaaS), you rent IT infrastructure—servers and virtual machines (VMs), storage, networks, operating systems—from a cloud provider on a pay-as-you-go basis.</li>
                <li><span style="color:black;font-weight:bold;font-size:16px">2.PaaS:</span> Platform as a service (PaaS) refers to cloud computing services that supply an on-demand environment for developing, testing, delivering, and managing software applications. PaaS is designed to make it easier for developers to quickly create web or mobile apps, without worrying 
                    about setting up or managing the underlying infrastructure of servers, storage, network, and databases needed for development.</li>

                <li><span style="color:black;font-weight:bold;font-size:16px">3.SaaS:</span>
                    Software as a service (SaaS) is a method for delivering software applications over the internet, on demand and typically on a subscription basis. With SaaS, cloud providers host and manage the software application and underlying infrastructure, and handle any maintenance, like software upgrades and security patching. </li>
                    <li><span style="color:black;font-weight:bold;font-size:16px">4.Serverless computing:</span>Overlapping with PaaS, serverless computing focuses on building app functionality without spending time continually managing the servers and infrastructure required to do so. The cloud provider handles the setup, capacity planning, and server management for you. Serverless architectures are highly scalable and 
                        event-driven, only using resources when a specific function or trigger occurs.</li>
    
                <center><img src="/techwix/assets/images/cloudcomputing5.png" alt="ux" style="width:600px;height:400px;border-radius:50px;margin-top:2%"></center>
               </div>
            </ul>


            <h1 class="main_heading m-3" style="margin-right: 2%;">Uses of cloud computing:</h1>
            <ul>
                <div style="margin-left: 1%;text-align: justify;padding:12px;margin-top:2%">
                
                    <p>If you use an online service to send email, edit documents, watch movies or TV, listen to music, play games, or store pictures and other files, it’s likely that cloud computing is making it all possible behind the scenes. A variety of organizations—from tiny startups to global corporations, 
                        government agencies to non-profits—have embraced cloud computing technology for all sorts of reasons.</p>
                
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Store, back up, and recover data:</span> Protect your data more cost-efficiently—and at massive scale—by transferring your data over the internet to an offsite cloud storage system that’s accessible from any location and any device.</li>
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Stream audio and video:</span> Connect with your audience anywhere, anytime, on any device with high-definition video and audio with global distribution.</li>
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Deliver software on demand:</span> Also known as software as a service (SaaS), on-demand software lets you offer the latest software versions and updates to customers—anytime they need, anywhere they are. </li>
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Analyze data:</span> Unify your data across teams, divisions, and locations in the cloud. Then use cloud services, such as machine learning and artificial intelligence, to uncover insights for more informed decisions. </li>
                <li class="para"><span style="color:black;font-size:16px;font-weight:bold">Embed intelligence:</span>  Use intelligent models to help engage customers and provide valuable insights from the data captured. </li>
                

            <h2 class="main_heading m-3" style="margin-right: 2%;">Benefits of cloud computing</h2>
            <p>Cloud computing operates on a similar principle as web-based email clients, allowing users to access all of the features and files of the system without having to keep the bulk of that system on their own computers.
                But more than that, they are using this technology to more efficiently run their organisations, better serve their customers, and dramatically increase their overall profit margins.
            </p>
                <div style="margin-left: 1%;text-align: justify;">
                    <ol>
                        <li>1.Faster time to market</li>
                        <li>2.Scalability and flexibility</li>
                        <li>3.Cost savings</li>
                        <li>4.Better collaboration</li>
                        <li>5.Security</li>
                        <li>6.Loss Prevention</li>
                        <li>7.Automatic Software Updates</li>
                    </ol>
