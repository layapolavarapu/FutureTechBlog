                <li class="para">Write clean code across the stack by following the best practices of the tools used.</li>

                <li class="para">Be up to date with the latest technologies and tools to make the best technology usage decisions.</li>  
               </div>
            </ul><br>

            <h1 class="main_heading m-3">What languages do full stack developers use?</h1>

            <p class="para" style="margin-left: 1%;text-align: justify;">Full stack developers are free to use any set of languages that are compatible with each other and the overall application framework. JavaScript is a popular language often used by full-stack developers as it’s one of the very few languages that can be used both on the front end and back end. Companies will most likely hire a full stack developer for smaller or medium-size projects. Some popular languages are:</p><br>
            <ul style="list-style-type:disc;">
                <div style="margin-left: 3%;text-align: justify;">
                <li class="para"><b>Front end:</b> HTML, CSS, JavaScript.</li>
                <li class="para"><b>Back end:</b> Python, Java, R, Ruby, Node.js, PHP.</li><br>
               </div>
            </ul>


