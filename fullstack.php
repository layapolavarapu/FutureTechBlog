            <h1 class="main_heading m-3">FullStack Development</h1>
            <p class="para" style="margin-left: 1%;text-align: justify;">The term "full stack development" describes the creation of a whole application, including the front end and back end. The user interface is on the front end, and the business logic and application operations are on the back end.</p>
            
            <center><img src="/techwix/assets/images/fullstack.png" class="image1 col-sm-6" style="width:300px;"></center>   
            <p class="para" style="margin-left: 1%;text-align: justify;">Consider a retail website. Users can browse or purchase specific items, delete or add items in cart, change their profile, and do many other things. All these actions require a front-end user interface (UI), as well as some business logic, written in the back-end.</p>
            <ul style="list-style-type:disc;margin-left: 3%;">
                <li>The website UI can be built using various, front-end technologies like HTML, CSS, Javascript.</li>
                <li>The back end is written in programming languages like Java or Python. Further, a good web application would need scalability, event handling, and routing, which are usually handled by libraries and frameworks like SpringBoot or Django.</li>
                <li>The back end also consists of logic that can connect the application to other services and databases. For example, all the user and transaction data is stored in a database through specific drivers handled on the back end.</li>
            </ul>
            <p class="para" style="margin-left: 1%;text-align: justify;">A full stack developer is one who can single-handedly implement both the front-end and back-end workflows, like placing the order or changing the user profile.</p><br><br>
            <center><img src="/techwix/assets/images/fullstack1.png"class="image1 col-sm-6"></center>
            
            
            <h1 class="main_heading m-3">What is a full stack developer and what do they do?</h1>

            <p class="para" style="margin-left: 1%;text-align: justify;">Full stack developers must have knowledge of an entire technology stack, i.e., the set of technologies that are used to build an end-to-end application quickly and efficiently. For example, if they want to build an application using the MEAN stack, they should know how to work with MongoDB, Express, Angular and Node.</p>

            <p class="para" style="margin-left: 1%;text-align: justify;">Full stack developers should be able to judge whether the selected technologies are the right choice for their project during the early phases. Some responsibilities of a full stack developer are to:</p>

            <ul style="list-style-type:disc;margin-left: 3%;">
                <div style="margin-left: 1%;text-align: justify;">
                <li class="para">Help in choosing the right technologies for the project development and testing both on the front end and the back end.</li>

                <li class="para">Write clean code across the stack by following the best practices of the tools used.</li>

                <li class="para">Be up to date with the latest technologies and tools to make the best technology usage decisions</li>
               </div>
            </ul>
            

